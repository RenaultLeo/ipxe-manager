/**
 * Reconstruit app/locale_values/{de,es,it,pt}.list.json depuis les chaînes uniques (tri EN)
 * et des paires [anglais, traduction]. Les clés sans traduction restent en anglais.
 */
import fs from "fs";

const uniq = JSON.parse(fs.readFileSync("tools/_unique_en_strings.json", "utf8"));
const enList = JSON.parse(fs.readFileSync("app/locale_values/_en.list.json", "utf8"));

function buildMap(pairs) {
  const m = Object.fromEntries(uniq.map((s) => [s, s]));
  for (const [en, tr] of pairs) {
    if (!(en in m)) {
      console.warn("WARN: unknown EN string in pairs:", en.slice(0, 80));
    }
    m[en] = tr;
  }
  return m;
}

function writeLocale(code, pairs) {
  const map = buildMap(pairs);
  const out = enList.map((s) => map[s] ?? s);
  fs.writeFileSync(`app/locale_values/${code}.list.json`, JSON.stringify(out, null, 0), "utf8");
  console.log("wrote", code, out.length);
}

