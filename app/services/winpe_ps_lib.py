from __future__ import annotations


def ps_inject_drivers_lib(drivers_json: str, drivers_root: str) -> str:
    return f"""# Generated by iPXE Manager — bibliotheque pilotes + unattend (offline)
param(
    [string]$TargetOS = 'W:\\',
    [string]$DriverProfileKey = ''
)

$ErrorActionPreference = 'Continue'
$script:WinpeInjectCliMode = ($MyInvocation.InvocationName -ne '.')

$script:WinpeDriversJson = '{drivers_json}'
$script:WinpeDriversRoot = '{drivers_root}'

function Get-WinpeDriverCatalog {{
    if (-not (Test-Path -LiteralPath $script:WinpeDriversJson)) {{ return $null }}
    $raw = Get-Content -LiteralPath $script:WinpeDriversJson -Raw -ErrorAction SilentlyContinue
    if (-not $raw) {{ return $null }}
    return ($raw | ConvertFrom-Json)
}}

function Get-DriverFolderForKey {{
    param($Catalog, [string]$Key)
    if (-not $Catalog -or -not $Key) {{ return $null }}
    $meta = $Catalog.$Key
    if (-not $meta) {{ return $null }}
    if ($meta.slug) {{ return Join-Path $script:WinpeDriversRoot $meta.slug }}
    $rel = ($meta.path -replace '/', '\\').Trim('\\')
    if ($rel -match '^drivers[\\/](.+)$') {{ return Join-Path $script:WinpeDriversRoot $Matches[1] }}
    return Join-Path $script:WinpeDriversRoot $rel
}}

function Try-DetectDriverProfile {{
    param($Catalog)
    if (-not $Catalog) {{ return $null, $null, 0 }}
    $hint = ''
    try {{
        $cs = Get-CimInstance Win32_ComputerSystem -ErrorAction SilentlyContinue
        if ($cs) {{ $hint = "$($cs.Manufacturer) $($cs.Model)".Trim() }}
    }} catch {{ }}
    if (-not $hint) {{ return $null, $hint, 0 }}
    $bestKey = $null
    $bestScore = 0
    foreach ($prop in $Catalog.PSObject.Properties) {{
        $k = $prop.Name
        $score = 0
        if ($hint -match [regex]::Escape($k)) {{ $score += 10 }}
        $slug = $prop.Value.slug
        if ($slug -and $hint -match [regex]::Escape($slug)) {{ $score += 8 }}
        if ($score -gt $bestScore) {{
            $bestScore = $score
            $bestKey = $k
        }}
    }}
    if ($bestScore -lt 8) {{ return $null, $hint, $bestScore }}
    return $bestKey, $hint, $bestScore
}}

function Install-WinpeDrivers {{
    param(
        [string]$TargetOS = 'W:\\',
        [string]$ProfileKey,
        [switch]$Quiet
    )
    if (-not $ProfileKey) {{
        if (-not $Quiet) {{ Write-Host 'Aucun profil pilote.' -ForegroundColor Yellow }}
        return $true
    }}
    $catalog = Get-WinpeDriverCatalog
    if (-not $catalog) {{
        if (-not $Quiet) {{ Write-Host 'drivers.json absent.' -ForegroundColor Yellow }}
        return $true
    }}
    $driverDir = Get-DriverFolderForKey $catalog $ProfileKey
    if (-not $driverDir -or -not (Test-Path -LiteralPath $driverDir)) {{
        Write-Host "Dossier pilotes introuvable : $ProfileKey" -ForegroundColor Red
        return $false
    }}
    $infCount = @(Get-ChildItem -LiteralPath $driverDir -Recurse -Filter '*.inf' -File -ErrorAction SilentlyContinue).Count
    if ($infCount -eq 0) {{
        Write-Host "Aucun .inf dans $driverDir" -ForegroundColor Red
        return $false
    }}
    if (-not $Quiet) {{
        Write-Host "Injection pilotes : $ProfileKey ($infCount .inf)" -ForegroundColor Cyan
    }}
    $imageRoot = $TargetOS.TrimEnd('\\')
    $dismArgs = @('/Image:' + $imageRoot, '/Add-Driver', '/Driver:' + $driverDir, '/Recurse')
    $p = Start-Process -FilePath 'dism.exe' -ArgumentList $dismArgs -Wait -PassThru -NoNewWindow
    if ($p.ExitCode -ne 0) {{
        Write-Host "DISM Add-Driver avertissement (code $($p.ExitCode)) — poursuite." -ForegroundColor Yellow
    }}
    return $true
}}

function Get-UnattendNsUri {{
    param([xml]$Doc)
    if ($Doc.DocumentElement -and $Doc.DocumentElement.NamespaceURI) {{
        return $Doc.DocumentElement.NamespaceURI
    }}
    return 'urn:schemas-microsoft-com:unattend'
}}

function Get-UnattendNsMgr {{
    param([xml]$Doc)
    $uri = Get-UnattendNsUri -Doc $Doc
    $mgr = New-Object System.Xml.XmlNamespaceManager($Doc.NameTable)
    $mgr.AddNamespace('u', $uri)
    $mgr.AddNamespace('wcm', 'http://schemas.microsoft.com/WMIConfig/2002/State')
    return $mgr, $uri
}}

function Get-UnattendWcmUri {{
    return 'http://schemas.microsoft.com/WMIConfig/2002/State'
}}

function Ensure-WcmOnShellComponent {{
    param($Shell)
    $wcmUri = Get-UnattendWcmUri
    if (-not $Shell.GetAttribute('xmlns:wcm')) {{
        $null = $Shell.SetAttribute('xmlns:wcm', $wcmUri)
    }}
}}

function New-UnattendElement {{
    param([xml]$Doc, [string]$LocalName, [string]$NsUri)
    if ($NsUri) {{ return $Doc.CreateElement($LocalName, $NsUri) }}
    return $Doc.CreateElement($LocalName)
}}

function Get-UnattendDirectChild {{
    param($Parent, [string]$LocalName)
    if (-not $Parent) {{ return $null }}
    foreach ($c in @($Parent.ChildNodes)) {{
        if ($c.NodeType -eq [System.Xml.XmlNodeType]::Element -and $c.LocalName -eq $LocalName) {{
            return $c
        }}
    }}
    return $null
}}

function Get-ShellSetupComponentsForPass {{
    param([xml]$Doc, [string]$Pass, $NsMgr)
    $settings = $Doc.SelectSingleNode("//u:settings[@pass='$Pass']", $NsMgr)
    if (-not $settings) {{ return @() }}
    return @($settings.SelectNodes('u:component[@name="Microsoft-Windows-Shell-Setup"]', $NsMgr))
}}

function Get-ShellSetupTemplateAttrs {{
    param([xml]$Doc, $NsMgr, [string]$NsUri)
    $ref = $Doc.SelectSingleNode('//u:component[@name="Microsoft-Windows-Shell-Setup"]', $NsMgr)
    if ($ref) {{
        return @{{
            processorArchitecture = $ref.GetAttribute('processorArchitecture')
            publicKeyToken = $ref.GetAttribute('publicKeyToken')
            language = $ref.GetAttribute('language')
            versionScope = $ref.GetAttribute('versionScope')
        }}
    }}
    return @{{
        processorArchitecture = 'amd64'
        publicKeyToken = '31bf3856ad364e35'
        language = 'neutral'
        versionScope = 'nonSxS'
    }}
}}

function Ensure-ShellSetupForPass {{
    param([xml]$Doc, [string]$Pass, $NsMgr, [string]$NsUri)
    $shells = Get-ShellSetupComponentsForPass -Doc $Doc -Pass $Pass -NsMgr $NsMgr
    if ($shells.Count -gt 0) {{ return $shells[0] }}
    $settings = $Doc.SelectSingleNode("//u:settings[@pass='$Pass']", $NsMgr)
    if (-not $settings) {{
        $settings = New-UnattendElement -Doc $Doc -LocalName 'settings' -NsUri $NsUri
        $null = $settings.SetAttribute('pass', $Pass)
        $Doc.DocumentElement.AppendChild($settings) | Out-Null
    }}
    $attrs = Get-ShellSetupTemplateAttrs -Doc $Doc -NsMgr $NsMgr -NsUri $NsUri
    $shell = New-UnattendElement -Doc $Doc -LocalName 'component' -NsUri $NsUri
    $null = $shell.SetAttribute('name', 'Microsoft-Windows-Shell-Setup')
    foreach ($k in $attrs.Keys) {{
        if ($attrs[$k]) {{ $null = $shell.SetAttribute($k, $attrs[$k]) }}
    }}
    $settings.AppendChild($shell) | Out-Null
    return $shell
}}

function Set-UnattendXmlText {{
    param($Parent, [xml]$Doc, [string]$NsUri, [string]$LocalName, [string]$Value)
    $node = Get-UnattendDirectChild -Parent $Parent -LocalName $LocalName
    if (-not $node) {{
        $node = New-UnattendElement -Doc $Doc -LocalName $LocalName -NsUri $NsUri
        $null = $Parent.AppendChild($node)
    }}
    $node.InnerText = $Value
    return $node
}}

function Set-UnattendPasswordNode {{
    param($Parent, [xml]$Doc, [string]$NsUri, [string]$Password, [switch]$AsPlainText, [switch]$KeepPlainTextMode)
    $pwEl = Get-UnattendDirectChild -Parent $Parent -LocalName 'Password'
    if (-not $pwEl) {{
        $pwEl = New-UnattendElement -Doc $Doc -LocalName 'Password' -NsUri $NsUri
        $null = $Parent.AppendChild($pwEl)
    }}
    $usePlain = $AsPlainText
    if ($KeepPlainTextMode) {{
        $ptKeep = Get-UnattendDirectChild -Parent $pwEl -LocalName 'PlainText'
        if ($ptKeep -and $ptKeep.InnerText -eq 'true') {{ $usePlain = $true }}
    }}
    if ($usePlain) {{
        Set-UnattendXmlText -Parent $pwEl -Doc $Doc -NsUri $NsUri -LocalName 'Value' -Value $Password | Out-Null
        Set-UnattendXmlText -Parent $pwEl -Doc $Doc -NsUri $NsUri -LocalName 'PlainText' -Value 'true' | Out-Null
    }} else {{
        $blob = ConvertTo-UnattendPasswordBlob -PlainText $Password
        Set-UnattendXmlText -Parent $pwEl -Doc $Doc -NsUri $NsUri -LocalName 'Value' -Value $blob | Out-Null
        Set-UnattendXmlText -Parent $pwEl -Doc $Doc -NsUri $NsUri -LocalName 'PlainText' -Value 'false' | Out-Null
    }}
}}

function Insert-ShellSetupChildBefore {{
    param($Shell, $NewNode, [string]$BeforeLocalName)
    $before = Get-UnattendDirectChild -Parent $Shell -LocalName $BeforeLocalName
    if ($before) {{
        $Shell.InsertBefore($NewNode, $before) | Out-Null
    }} else {{
        $Shell.AppendChild($NewNode) | Out-Null
    }}
}}

function Set-UnattendComputerName {{
    param([xml]$Doc, $NsMgr, [string]$NsUri, [string]$ComputerName)
    $updated = 0
    foreach ($pass in @('specialize', 'generalize')) {{
        $nodes = $Doc.SelectNodes("//u:settings[@pass='$pass']//u:ComputerName", $NsMgr)
        foreach ($n in @($nodes)) {{
            $n.InnerText = $ComputerName
            $updated++
        }}
    }}
    if ($updated -eq 0) {{
        $shell = Ensure-ShellSetupForPass -Doc $Doc -Pass 'specialize' -NsMgr $NsMgr -NsUri $NsUri
        Set-UnattendXmlText -Parent $shell -Doc $Doc -NsUri $NsUri -LocalName 'ComputerName' -Value $ComputerName | Out-Null
        $updated = 1
    }}
    return $updated
}}

function Test-UnattendLocalAccountExistsInDoc {{
    param([xml]$Doc, $NsMgr, [string]$User)
    $shells = Get-ShellSetupComponentsForPass -Doc $Doc -Pass 'oobeSystem' -NsMgr $NsMgr
    foreach ($shell in $shells) {{
        $ua = Get-UnattendDirectChild -Parent $shell -LocalName 'UserAccounts'
        if (-not $ua) {{ continue }}
        $la = Get-UnattendDirectChild -Parent $ua -LocalName 'LocalAccounts'
        if (-not $la) {{ continue }}
        foreach ($node in @($la.SelectNodes('u:LocalAccount', $NsMgr))) {{
            $n = Get-UnattendDirectChild -Parent $node -LocalName 'Name'
            if ($n -and $n.InnerText -ieq $User) {{ return $true }}
        }}
    }}
    return $false
}}

function Add-UnattendOobeLocalAccount {{
    param([xml]$Doc, $NsMgr, [string]$NsUri, [string]$User, [string]$Password)
    if (Test-UnattendLocalAccountExistsInDoc -Doc $Doc -NsMgr $NsMgr -User $User) {{
        return $false
    }}
    $shell = (Get-ShellSetupComponentsForPass -Doc $Doc -Pass 'oobeSystem' -NsMgr $NsMgr | Select-Object -First 1)
    if (-not $shell) {{
        $shell = Ensure-ShellSetupForPass -Doc $Doc -Pass 'oobeSystem' -NsMgr $NsMgr -NsUri $NsUri
    }}
    Ensure-WcmOnShellComponent -Shell $shell
    $wcmUri = Get-UnattendWcmUri
    $userAccounts = Get-UnattendDirectChild -Parent $shell -LocalName 'UserAccounts'
    if (-not $userAccounts) {{
        $userAccounts = New-UnattendElement -Doc $Doc -LocalName 'UserAccounts' -NsUri $NsUri
        Insert-ShellSetupChildBefore -Shell $shell -NewNode $userAccounts -BeforeLocalName 'AutoLogon'
    }}
    $localAccounts = Get-UnattendDirectChild -Parent $userAccounts -LocalName 'LocalAccounts'
    if (-not $localAccounts) {{
        $localAccounts = New-UnattendElement -Doc $Doc -LocalName 'LocalAccounts' -NsUri $NsUri
        $userAccounts.AppendChild($localAccounts) | Out-Null
    }}
    $acct = New-UnattendElement -Doc $Doc -LocalName 'LocalAccount' -NsUri $NsUri
    $null = $acct.SetAttribute('action', $wcmUri, 'add')
    $localAccounts.AppendChild($acct) | Out-Null
    Set-UnattendPasswordNode -Parent $acct -Doc $Doc -NsUri $NsUri -Password $Password -AsPlainText
    Set-UnattendXmlText -Parent $acct -Doc $Doc -NsUri $NsUri -LocalName 'Description' -Value ('Compte ajoute par iPXE Manager — ' + $User) | Out-Null
    Set-UnattendXmlText -Parent $acct -Doc $Doc -NsUri $NsUri -LocalName 'DisplayName' -Value $User | Out-Null
    Set-UnattendXmlText -Parent $acct -Doc $Doc -NsUri $NsUri -LocalName 'Group' -Value 'Administrators' | Out-Null
    Set-UnattendXmlText -Parent $acct -Doc $Doc -NsUri $NsUri -LocalName 'Name' -Value $User | Out-Null
    return $true
}}

function Update-UnattendAutoLogon {{
    param([xml]$Doc, $Shell, [string]$NsUri, [string]$User, [string]$Password)
    Ensure-WcmOnShellComponent -Shell $Shell
    $auto = Get-UnattendDirectChild -Parent $Shell -LocalName 'AutoLogon'
    if (-not $auto) {{
        $auto = New-UnattendElement -Doc $Doc -LocalName 'AutoLogon' -NsUri $NsUri
        $Shell.AppendChild($auto) | Out-Null
    }}
    Set-UnattendXmlText -Parent $auto -Doc $Doc -NsUri $NsUri -LocalName 'Enabled' -Value 'true' | Out-Null
    Set-UnattendXmlText -Parent $auto -Doc $Doc -NsUri $NsUri -LocalName 'Username' -Value $User | Out-Null
    Set-UnattendPasswordNode -Parent $auto -Doc $Doc -NsUri $NsUri -Password $Password -AsPlainText
    Set-UnattendXmlText -Parent $auto -Doc $Doc -NsUri $NsUri -LocalName 'LogonCount' -Value '1' | Out-Null
    return $true
}}

function Save-UnattendDocument {{
    param([xml]$Doc, [string]$Path)
    $settings = New-Object System.Xml.XmlWriterSettings
    $settings.Encoding = New-Object System.Text.UTF8Encoding $true
    $settings.Indent = $true
    $settings.OmitXmlDeclaration = $false
    $writer = [System.Xml.XmlWriter]::Create($Path, $settings)
    try {{ $Doc.Save($writer) }} finally {{ $writer.Close() }}
}}

function ConvertTo-UnattendPasswordBlob {{
    param([string]$PlainText)
    if (-not $PlainText) {{ return '' }}
    $bytes = [System.Text.Encoding]::Unicode.GetBytes($PlainText)
    return [Convert]::ToBase64String($bytes)
}}

function Find-OfflineUnattendPath {{
    param([string]$TargetOS = 'W:\\')
    $candidates = @(
        (Join-Path $TargetOS 'Windows\\Panther\\unattend.xml'),
        (Join-Path $TargetOS 'Windows\\System32\\Sysprep\\unattend.xml'),
        (Join-Path $TargetOS 'unattend.xml')
    )
    foreach ($p in $candidates) {{
        if (Test-Path -LiteralPath $p) {{ return $p }}
    }}
    $found = @(Get-ChildItem -LiteralPath $TargetOS -Recurse -Filter 'unattend.xml' -File -ErrorAction SilentlyContinue | Select-Object -First 1)
    if ($found) {{ return $found.FullName }}
    return $null
}}

function Get-UiLanguageById {{
    param([string]$LanguageId)
    if (-not $LanguageId) {{ return $null }}
    foreach ($p in @($script:WinpeUiLanguages)) {{
        if ($p.id -eq $LanguageId) {{ return $p }}
    }}
    return $null
}}

function Get-KeyboardById {{
    param([string]$KeyboardId)
    if (-not $KeyboardId) {{ return $null }}
    foreach ($p in @($script:WinpeKeyboardLayouts)) {{
        if ($p.id -eq $KeyboardId) {{ return $p }}
    }}
    return $null
}}

function Get-RegionById {{
    param([string]$RegionId)
    if (-not $RegionId) {{ return $null }}
    foreach ($p in @($script:WinpeRegions)) {{
        if ($p.id -eq $RegionId) {{ return $p }}
    }}
    return $null
}}

function Get-InternationalComponentName {{
    param([bool]$WinPE)
    if ($WinPE) {{ return 'Microsoft-Windows-International-Core-WinPE' }}
    return 'Microsoft-Windows-International-Core'
}}

function Ensure-InternationalComponentForPass {{
    param([xml]$Doc, [string]$Pass, $NsMgr, [string]$NsUri, [bool]$WinPE)
    $compName = Get-InternationalComponentName -WinPE $WinPE
    $settings = $Doc.SelectSingleNode("//u:settings[@pass='$Pass']", $NsMgr)
    if (-not $settings) {{
        $settings = New-UnattendElement -Doc $Doc -LocalName 'settings' -NsUri $NsUri
        $null = $settings.SetAttribute('pass', $Pass)
        $Doc.DocumentElement.AppendChild($settings) | Out-Null
    }}
    $comp = $settings.SelectSingleNode("u:component[@name='$compName']", $NsMgr)
    if ($comp) {{ return $comp }}
    $attrs = Get-ShellSetupTemplateAttrs -Doc $Doc -NsMgr $NsMgr -NsUri $NsUri
    $comp = New-UnattendElement -Doc $Doc -LocalName 'component' -NsUri $NsUri
    $null = $comp.SetAttribute('name', $compName)
    foreach ($k in $attrs.Keys) {{
        if ($attrs[$k]) {{ $null = $comp.SetAttribute($k, $attrs[$k]) }}
    }}
    $settings.AppendChild($comp) | Out-Null
    return $comp
}}

function Set-InternationalFieldsOnComponent {{
    param($Comp, [xml]$Doc, [string]$NsUri, $UiLang, $Region, [string]$InputLocale, [bool]$IncludeSetupUiLanguage)
    if ($IncludeSetupUiLanguage) {{
        $sul = Get-UnattendDirectChild -Parent $Comp -LocalName 'SetupUILanguage'
        if (-not $sul) {{
            $sul = New-UnattendElement -Doc $Doc -LocalName 'SetupUILanguage' -NsUri $NsUri
            if ($Comp.FirstChild) {{
                $Comp.InsertBefore($sul, $Comp.FirstChild) | Out-Null
            }} else {{
                $Comp.AppendChild($sul) | Out-Null
            }}
        }}
        Set-UnattendXmlText -Parent $sul -Doc $Doc -NsUri $NsUri -LocalName 'UILanguage' -Value $UiLang.setup_ui_language | Out-Null
    }}
    Set-UnattendXmlText -Parent $Comp -Doc $Doc -NsUri $NsUri -LocalName 'InputLocale' -Value $InputLocale | Out-Null
    Set-UnattendXmlText -Parent $Comp -Doc $Doc -NsUri $NsUri -LocalName 'SystemLocale' -Value $Region.system_locale | Out-Null
    Set-UnattendXmlText -Parent $Comp -Doc $Doc -NsUri $NsUri -LocalName 'UILanguage' -Value $UiLang.ui_language | Out-Null
    Set-UnattendXmlText -Parent $Comp -Doc $Doc -NsUri $NsUri -LocalName 'UILanguageFallback' -Value $UiLang.ui_language_fallback | Out-Null
    Set-UnattendXmlText -Parent $Comp -Doc $Doc -NsUri $NsUri -LocalName 'UserLocale' -Value $Region.user_locale | Out-Null
}}

function Set-UnattendLocaleSettings {{
    param([xml]$Doc, $NsMgr, [string]$NsUri, $UiLang, $Region, [string]$InputLocale)
    if (-not $UiLang -or -not $Region -or -not $InputLocale) {{ return }}
    $pe = Ensure-InternationalComponentForPass -Doc $Doc -Pass 'windowsPE' -NsMgr $NsMgr -NsUri $NsUri -WinPE $true
    Set-InternationalFieldsOnComponent -Comp $pe -Doc $Doc -NsUri $NsUri -UiLang $UiLang -Region $Region -InputLocale $InputLocale -IncludeSetupUiLanguage $true
    $oob = Ensure-InternationalComponentForPass -Doc $Doc -Pass 'oobeSystem' -NsMgr $NsMgr -NsUri $NsUri -WinPE $false
    Set-InternationalFieldsOnComponent -Comp $oob -Doc $Doc -NsUri $NsUri -UiLang $UiLang -Region $Region -InputLocale $InputLocale -IncludeSetupUiLanguage $false
}}

function Update-OfflineUnattend {{
    param(
        [string]$TargetOS = 'W:\\',
        [string]$ComputerName = '',
        [string]$LocalUser = '',
        [string]$LocalPassword = '',
        [string]$LanguageId = '',
        [string]$RegionId = '',
        [string]$KeyboardId = '',
        [string]$LocaleId = ''
    )
    $path = Find-OfflineUnattendPath -TargetOS $TargetOS
    if (-not $path) {{
        Write-Host 'unattend.xml introuvable dans l image — nom machine / user non modifies.' -ForegroundColor Yellow
        return $false
    }}
    Write-Host "Personnalisation : $path" -ForegroundColor Cyan
    $doc = New-Object System.Xml.XmlDocument
    $doc.PreserveWhitespace = $true
    $doc.Load($path)
    $nsMgr, $nsUri = Get-UnattendNsMgr -Doc $doc
    $changed = $false

    $legacy = if ($LocaleId -and $LocaleId.Trim()) {{ $LocaleId.Trim() }} else {{ '' }}
    $langId = if ($LanguageId -and $LanguageId.Trim()) {{ $LanguageId.Trim() }} elseif ($legacy) {{ $legacy }} else {{ $script:WinpeDefaultUiLanguageId }}
    $regionId = if ($RegionId -and $RegionId.Trim()) {{ $RegionId.Trim() }} elseif ($legacy) {{ $legacy }} else {{ $script:WinpeDefaultRegionId }}
    $kbId = if ($KeyboardId -and $KeyboardId.Trim()) {{ $KeyboardId.Trim() }} elseif ($legacy) {{ $legacy }} else {{ $script:WinpeDefaultKeyboardId }}
    $uiLang = Get-UiLanguageById -LanguageId $langId
    $region = Get-RegionById -RegionId $regionId
    $keyboard = Get-KeyboardById -KeyboardId $kbId
    if ($uiLang -and $region -and $keyboard) {{
        Set-UnattendLocaleSettings -Doc $doc -NsMgr $nsMgr -NsUri $nsUri -UiLang $uiLang -Region $region -InputLocale $keyboard.input_locale
        Write-Host "Langue interface : $($uiLang.label) ($($uiLang.id))" -ForegroundColor Green
        Write-Host "Region (formats) : $($region.label) ($($region.id))" -ForegroundColor Green
        Write-Host "Clavier : $($keyboard.label) ($($keyboard.input_locale))" -ForegroundColor Green
        $changed = $true
    }} else {{
        if (-not $uiLang) {{ Write-Host "Langue interface inconnue : $langId — non modifiee." -ForegroundColor Yellow }}
        if (-not $region) {{ Write-Host "Region inconnue : $regionId — non modifiee." -ForegroundColor Yellow }}
        if (-not $keyboard) {{ Write-Host "Clavier inconnu : $kbId — non modifie." -ForegroundColor Yellow }}
    }}

    if ($ComputerName -and $ComputerName.Trim()) {{
        $cn = $ComputerName.Trim()
        if ($cn.Length -gt 15) {{ $cn = $cn.Substring(0, 15) }}
        $cn = $cn -replace '[^a-zA-Z0-9\\-]', ''
        if ($cn) {{
            $n = Set-UnattendComputerName -Doc $doc -NsMgr $nsMgr -NsUri $nsUri -ComputerName $cn
            Write-Host "Nom machine : $cn" -ForegroundColor Green
            $changed = $true
        }}
    }}

    if ($LocalUser -and $LocalUser.Trim()) {{
        $user = $LocalUser.Trim() -replace '[^a-zA-Z0-9._-]', ''
        if (-not $user) {{
            Write-Host 'Nom utilisateur invalide — compte local ignore.' -ForegroundColor Yellow
        }} elseif (-not $LocalPassword) {{
            Write-Host 'Mot de passe requis pour un compte local — compte ignore (cochez et saisissez un mot de passe).' -ForegroundColor Yellow
        }} else {{
            if (Test-UnattendLocalAccountExistsInDoc -Doc $doc -NsMgr $nsMgr -User $user) {{
                Write-Host "Compte « $user » deja dans le XML — aucune modification." -ForegroundColor Yellow
            }} else {{
                $added = Add-UnattendOobeLocalAccount -Doc $doc -NsMgr $nsMgr -NsUri $nsUri -User $user -Password $LocalPassword
                if (-not $added) {{
                    Write-Host "Impossible d ajouter le compte $user dans oobeSystem." -ForegroundColor Red
                }} else {{
                    Write-Host "Compte ajoute (oobeSystem, wcm:action=add) : $user — IT-LOCAL / comptes master intacts." -ForegroundColor Green
                    $shellOobe = (Get-ShellSetupComponentsForPass -Doc $doc -Pass 'oobeSystem' -NsMgr $nsMgr | Select-Object -First 1)
                    if ($shellOobe) {{
                        Update-UnattendAutoLogon -Doc $doc -Shell $shellOobe -NsUri $nsUri -User $user -Password $LocalPassword | Out-Null
                        Write-Host "AutoLogon : $user (meme mot de passe en clair que le compte)." -ForegroundColor Green
                    }}
                    $changed = $true
                }}
            }}
        }}
    }}

    if ($changed) {{
        Save-UnattendDocument -Doc $doc -Path $path
    }} else {{
        Write-Host 'Aucune modification unattend (valeurs par defaut du master).' -ForegroundColor Gray
    }}
    return $true
}}

if ($script:WinpeInjectCliMode) {{
    if ($DriverProfileKey) {{
        $ok = Install-WinpeDrivers -TargetOS $TargetOS -ProfileKey $DriverProfileKey
        if (-not $ok) {{ exit 1 }}
    }}
    exit 0
}}
"""


